  <div class="content">
    
<div style="margin-top: 24px; font-size: 14px; line-height: 22px; color: #444444;">
 <p style="margin-top: 0px;"><span style="color: #ef981c; font-size: 1.5em; font-weight: bold;">HGNC</span> is responsible for approving unique symbols and names for human loci, including protein coding genes, ncRNA genes and pseudogenes, to allow unambiguous scientific communication.</p>
 <p style="margin-top: 0px;"><span style="color: #ef981c; font-size: 1.5em; font-weight: bold;">genenames.org</span> is a curated online repository of HGNC-approved gene nomenclature, gene families and associated resources including links to genomic, proteomic and phenotypic information.</p>
 <p style="margin-top: 0px;"><span style="color: #ef981c; font-size: 1.5em; font-weight: bold;">Search</span> our catalogue of more than 39,000 symbol reports using our improved search engine (see <a href="/help/search">Search help</a>), search lists of symbols using our <a href="/cgi-bin/symbol_checker">Multi-symbol checker</a> and identify possible orthologs using our <a href="/cgi-bin/hcop">HCOP tool</a>.</p>
 <p style="margin-top: 0px;"><span style="color: #ef981c; font-size: 1.5em; font-weight: bold;">Download</span> our ready-made data files from our <a href="/cgi-bin/statistics">Statistics and Downloads</a> page, create your own datasets using our <a href="/cgi-bin/download">Custom Downloads</a> tool or write a script/program utilising our <a href="/help/rest-web-service-help">REST service</a>.</p>
 <p style="margin-top: 0px;"><span style="color: #ef981c; font-size: 1.5em; font-weight: bold;">Submit</span> your <a href="/cgi-bin/request">gene symbol and name proposals</a> to us to be accredited with HGNC approved nomenclature for use in publications, databases and presentations.</p>
